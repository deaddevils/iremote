package com.iremote.asiainfo.vo;

public class ReportParse {
	private String replySeriNum;
	private int replyResult;
	private String deviceId;
	private Object stateList;
	
	public String getReplySeriNum() {
		return replySeriNum;
	}
	public void setReplySeriNum(String replySeriNum) {
		this.replySeriNum = replySeriNum;
	}
	public int getReplyResult() {
		return replyResult;
	}
	public void setReplyResult(int replyResult) {
		this.replyResult = replyResult;
	}
	public String getDeviceId() {
		return deviceId;
	}
	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}
	public Object getStateList() {
		return stateList;
	}
	public void setStateList(Object stateList) {
		this.stateList = stateList;
	}
}
