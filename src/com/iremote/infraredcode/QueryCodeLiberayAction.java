	public String execute()
	{
		if ( "TV".equalsIgnoreCase(this.devicetype))
			return this.queryTvCodeLiberay();
		else if ( "AC".equalsIgnoreCase(this.devicetype) || this.devicetype == null || this.devicetype.length() == 0 )
			queryACCodeLiberay();
		else if ( "STB".equalsIgnoreCase(this.devicetype))
			return this.querySTBCodeLiberay();
		return Action.SUCCESS;
	}
	
	private String queryACCodeLiberay()
	{
		QueryACCodeLibeary qa = new QueryACCodeLibeary();
		qa.setCode(this.code);
		qa.setDeviceid(this.deviceid);
		qa.setDevicetype(this.devicetype);
		qa.setProductor(this.productor);
		
		String rst = qa.execute();
		
		this.resultCode = qa.getResultCode();
		this.codeid = qa.getCodeid();
		this.codelibery = qa.getCodelibery();
		return rst;
	}
	
	private String queryTvCodeLiberay()
	{
		CodeQueryBase cq = tvsearchcodemap.get(productor.toLowerCase());
		if ( cq == null )
			return Action.SUCCESS;
		
		int sequence = cq.queryCodeLiberay(code);
		if ( sequence == -1 )
			return Action.SUCCESS;
		
		int index = TvCodeLiberay.getProductorLibery(productor, sequence);
		
		codelibery = CodeLiberayHelper.composeTVCodeLiberay(TvCodeLiberay.getCodeLiberay(index)) ;
		codeid = String.format("TV_%d", index);
		resultCode = ErrorCodeDefine.SUCCESS;
		
		return Action.SUCCESS;
	}
	
	private String querySTBCodeLiberay()
	{
		CodeQueryBase cq = stbsearchcodemap.get(productor.toLowerCase());
		if ( cq == null )
			return Action.SUCCESS;
		
		int sequence = cq.queryCodeLiberay(code);
		if ( sequence == -1 )
			return Action.SUCCESS;
		
		int index = STBCodeLiberay.getProductorLibery(productor, sequence);
		
		codelibery = CodeLiberayHelper.composeTVCodeLiberay(TvCodeLiberay.getCodeLiberay(index)) ;
		codeid = String.format("STB_%d", index);
		resultCode = ErrorCodeDefine.SUCCESS;
		
		return Action.SUCCESS;
	}
	
